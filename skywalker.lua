local Tunnel = module("vrp","lib/Tunnel")
local Proxy = module("vrp","lib/Proxy")
vRP = Proxy.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
--[ CONEXÃO ]----------------------------------------------------------------------------------------------------------------------------
-----------------------------------------------------------------------------------------------------------------------------------------
raD = {}
Tunnel.bindInterface("vrp_radio",raD)
-----------------------------------------------------------------------------------------------------------------------------------------
--[ PERMISSÃO ][ Copie a função abaixo quantas vezes quiser, renomeando a e redefinindo a sua permissão. ]-------------------------------
-----------------------------------------------------------------------------------------------------------------------------------------
function raD.permissaoNome()
	local source = source
	local user_id = vRP.getUserId(source)
	return vRP.hasPermission(user_id,"nome.permissao")
end
-----------------------------------------------------------------------------------------------------------------------------------------
--[ COMANDO ]----------------------------------------------------------------------------------------------------------------------------
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterCommand('radio',function(source,args,rawCommand)
	local source = source
	local user_id = vRP.getUserId(source)
	if vRP.getInventoryItemAmount(user_id,"radio") >= 1 then
		TriggerClientEvent("vrp_radio:onradio", source)
	else
		TriggerClientEvent("Notify",source,"aviso","Você <b>não possui</b> um <b>rádio</b> na mochila.") 
	end
end)